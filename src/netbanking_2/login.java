    private void jButton_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_loginActionPerformed
        // TODO add your handling code here:
        String email=jTextField_email.getText().trim();
        System.out.println(email);
        
        String password = String.valueOf(jPasswordField1.getPassword()).trim();
        System.out.println(password);
        
        if(email.equals(""))
        {
            jLabel_error_message.setText("Please enter your email");
        }else if(password.equals(""))
        {
            jLabel_error_message.setText("Please enter your password");
        }else
        {
            jLabel_error_message.setText("Incorrect email or password");
        }
        
        
        
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/db_internetbanking","root","");
            String sql="select * from tbl_reg where email = '"+email+"' && password = '"+password+"' ";
            System.out.println(sql);
            Statement stmt=con.createStatement();
            ResultSet rs=stmt.executeQuery(sql);
            //System.out.println(rs);
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "Login Successful");
                this.dispose();
                dashboard d=new dashboard();
                d.setVisible(true);
            }
        } catch (Exception e) {
            System.out.println("Error "+e);
        }
        
        
        
        //show jpanel message
        timerDown.start();
    }//GEN-LAST:event_jButton_loginActionPerformed

